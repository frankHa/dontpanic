#include "project.hpp"
// ---------------------------------------------------------------------------------
namespace dp
{
  // ---------------------------------------------------------------------------------
  ///ctor
  project::project()
      : _M_id ( "0" )
      , _M_name ( "" )
      , _M_visible ( true )
      , _M_creation_date ( QDateTime::currentDateTime() ) {}
  // ---------------------------------------------------------------------------------
  QString const& project::id() const
  {
    return _M_id;
  }
  // ---------------------------------------------------------------------------------
  QString const& project::name() const
  {
    return _M_name;
  }
  // ---------------------------------------------------------------------------------
  bool project::is_visible() const
  {
    return _M_visible;
  }
  // ---------------------------------------------------------------------------------
  QDateTime const& project::creation_date() const
  {
    return _M_creation_date;
  }
  // ---------------------------------------------------------------------------------
}
