#include "category.h"
// ---------------------------------------------------------------------------------
namespace dp
{
  // ---------------------------------------------------------------------------------
  namespace reports
  {
    // ---------------------------------------------------------------------------------
    namespace grouping
    {
      category::category()
          : embedded ( new null() ) {}
      // ---------------------------------------------------------------------------------
      category::category ( category *embed )
          : embedded ( embed ) {}
      // ---------------------------------------------------------------------------------
      category::~category() {delete embedded;}
      // ---------------------------------------------------------------------------------
      void category::embed ( category *cat )
      {
        if ( embedded == 0 )
        {
          embedded = cat;
        }
        else
        {
          embedded->embed ( cat );
        }
      }
      // ---------------------------------------------------------------------------------
    }//grouping
    // ---------------------------------------------------------------------------------
  }//reports
  // ---------------------------------------------------------------------------------
}//dp
// ---------------------------------------------------------------------------------
