#ifndef DP_SUCCESS_HPP
#define DP_SUCCESS_HPP
// ---------------------------------------------------------------------------------
namespace dp
{
  // ---------------------------------------------------------------------------------
  class success
  {
      // ---------------------------------------------------------------------------------
    public:
      // ---------------------------------------------------------------------------------
      enum state { succeeded = 0, failed = 1};
      // ---------------------------------------------------------------------------------
    public:
      // ---------------------------------------------------------------------------------
      success ( state _state ) : _M_success ( _state == succeeded) {}
      // ---------------------------------------------------------------------------------
      success ( bool successful ) : _M_success ( successful) {}
      // ---------------------------------------------------------------------------------
      bool was_successful() const
      {
        return _M_success;
      }
      // ---------------------------------------------------------------------------------
      bool has_failed() const
      {
        return !_M_success;
      }
      // ---------------------------------------------------------------------------------
    private:
      // ---------------------------------------------------------------------------------
      bool _M_success;
      // ---------------------------------------------------------------------------------
  };
  // ---------------------------------------------------------------------------------
  inline success successful()
  {
    return success(success::succeeded);
  }
  // ---------------------------------------------------------------------------------
  inline success error()
  {
    return success(success::failed);
  }
  // ---------------------------------------------------------------------------------
}//dp
// ---------------------------------------------------------------------------------
#endif //DP_SUCCESS_HPP
